package day03;

public class OperateDemo5 {
    public static void main(String[] args){
        int x=0,y;
        y=(x>1)?100:200;
        /*
        条件表达式？表达式1：表达式2
        三元运算符：如果条件表达式的值为真，则结果为表达式1的值
           如果条件表达式的值为假，则结果为表达式2的值。
         */
        System.out.println("y="+y);
    }
}
